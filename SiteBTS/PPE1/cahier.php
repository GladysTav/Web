<h2>Le Closed-Loop Marketing (CLM)</h2>
<h3>Cahier des charges :</h3>
<p>Nous avons rédigé un cahier des charges afin de déterminer les différentes
caractéristiques nécessaire et les limites du futur matériel informatique dans lequel
nous allons investir pour nos visiteurs.</p>
<h4>Contexte et définition du problème :</h4><p>
Ce cahier des charges a pour but de mener à la solution idéale pour améliorer la qualité
des visites médicales, rendu aux prescripteurs, par l’acquisition d’équipements
adaptés.</p><p>
Notre entreprise comprend 480 visiteurs médicaux en France et 60 dans les
départements et territoires d’outre-mer, soit un total de 540 visiteurs.
Actuellement, ils sont libres dans leur choix d’équipement professionnel et peuvent
même utiliser leur appareil personnel.</p><p>
Cependant, après notre choix d’utiliser l’outil de CLM de Viseven, nous devons nous
assurer de la compatibilité des appareils avec le logiciel choisi et ainsi proposer un
appareil plus adapté au travail des visiteurs médicaux.
Nous ne pouvons pas nous contenter de choisir un appareil compatible avec le CLM, car
les visiteurs ont des besoins spéciaux que nous nous devons de respecter afin de ne pas
entraver leur travail.</p>
<h4>Objectif :</h4><p>
L’objectif de ce projet est de livrer aux visiteurs un équipement adapté à leur situation
de travail et supportant le CLM de Viseven qui est compatible sur Windows, IOS et
Android.</p><p>
Les visiteurs médicaux sont des attachés commerciaux qui représentent un ou
plusieurs laboratoires pharmaceutiques. Ils démarchent des prescripteurs (médecins,
dentistes, vétérinaires ou pharmaciens) afin de promouvoir leur produits et de les faire
prescrire aux patients.</p><p>
Ils ne sont en aucun cas vendeur. Leur but est tout simplement de récupérer les avis des
prescripteurs sur les médicaments mis à disposition.
Cette étape devra donc mener à un choix d’équipement spécifique, pleinement adapté
au rôle des visiteurs.</p>

<h4>Périmètre, limites :</h4><p>
Les visiteurs médicaux sont constamment en déplacement, et ne peuvent donc se
permettre de devoir travailler avec un équipement encombrant.</p><p>
L’appareil choisi devra donc être portable, et suffisamment petit pour être transporté
sans aucun souci.</p><p>
Il devra également avoir une autonomie assez longue afin de pourvoir fonctionner une
majeur partie de la journée, car les visiteurs sont souvent en mouvement.
Dans le cadre de leurs nombreux déplacements, leur travail devra pouvoir de faire sans
connexion internet, mais l’appareil devra en disposer afin de synchroniser les
informations avec les prescripteurs.</p><p>
Nous privilégierons également un environnement informatique connu tel que Windows
afin de faciliter la prise en main de l’équipement par les visiteurs et également pour
s’assurer de la compatibilité avec le logiciel de CLM.</p>
<h4>Enveloppe budgétaire :</h4><p>
Pour cet équipement, nous avons un budget de 1 000€ H.T. par visiteur. Ce qui nous fait
un budget total de 540.000€ pour équiper tous nos visiteurs médicaux.</p>



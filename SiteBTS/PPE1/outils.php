<h2>Le Closed-Loop Marketing (CLM)</h2>
<h3>Outils de communication et bilan :</h3>
<p>
Tout au long de ce projet, nous avons communiqué par internet en utilisant un groupe
Messenger et nous avons créé un Drive Google afin de pouvoir nous partager les
documents.</p><p>
Nous nous sommes organisés pour avancer progressivement en répartissant les
tâches selon les affinités de chacun.</p><p>
Cette méthode nous a permis de faire progresser chaque partie du projet de manière
efficace car chaque membre ne disposais pas des mêmes compétences ni des mêmes
préférences.</p><p>
Après avoir fait de longue recherche sur les CLM et CRM, nous avons cherché
différentes solutions, avant de peser le pour et le contre de chacune pour en ressortir
les deux meilleures et ensuite les comparer pour rédiger une étude comparative.</p><p>
Puis nous avons fait des tests sur la solution choisie afin de pouvoir s’assurer que la
solution choisie correspondait au projet que nous voulions réaliser.</p><p>
Une fois l’outil choisi, nous avons inclut les plateformes qu’il supporte dans les
conditions du choix d’équipement de la seconde étape, qui consistait à rédiger un
cahier des charges.</p><p>
Après plusieurs recherches, nous nous sommes mis d’accord sur un équipement
correspondant à la fois au demandeur et au logiciel.</p><p>
Une fois tous ces éléments déterminés, nous avons pu rédiger tout ce qui était
nécessaire de rendre pour notre projet.</p><p>
Nous avons réparti les parties à rédiger, parfois même des bouts de parties, en
demandant à chaque fois l’avis du groupe sur notre travail. Nous avons ainsi pu nous
entraider pour améliorer nos capacités rédactionnelles, et apprécier les compétences
de chacun, ce qui nous permettra de mieux répartir les tâches à l’avenir.</p><p>
Ainsi se clôture notre premier projet. GSB dispose désormais des solutions software
et hardware pour optimiser le travail de ses visiteurs médicaux.</p>

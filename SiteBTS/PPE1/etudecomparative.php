<h2>Le Closed-Loop Marketing (CLM)</h2>
<h3>Etude comparative :</h3>
<p>Le Closed Loop Marketing (CLM) est une version dérivée du CRM. Le CRM est un logiciel
de gestion de relations clients. Il permet d’améliorer le traitement et l’analyse des
données concernant les clients. Les informations obtenues sont stockées et servent
ainsi à améliorer son discours commercial, à mieux cibler ses actions, à conquérir plus
efficacement de nouveaux marchés et à fidéliser les clients existants.</p><p>
La maîtrise de la connaissance des clients permet de contrôler le suivi des échanges et
ainsi augmenter sensiblement le chiffre d’affaire.</p><p>
Le CLM, lui, n’a pas pour but d’augmenter le chiffre d’affaire, mais d’adapter le
bon contenu aux bonnes personnes. L’objectif est de comprendre le marché et de
savoir ainsi ce qui fonctionne, ou pas, et pourquoi, grâce aux retours des clients.</p><p>
Afin de se démarquer et d’améliorernl’expérience des prescripteurs lors des visites médicales, l’industrie pharmaceutique a décidé de dématérialiser le papier pour diverses raisons.
Premièrement parce que le manuel d'utilisation
médicale et technique concernant la
commercialisation d'un nouveau produit
thérapeutique sur le secteur peut être
importante, et de ce fait imposante.</p>
<img src="closedloopmarketingcycle.jpg" style="float: right;" width="300px" height="300px">
<p>
Deuxièmement, il est compliqué de noter la
performance et la pertinence des produits
après une unique réunion, en plus du fait que le
mode d'emploi a des chances d’être mis de côté
voire jeté et non lu. Le papier reste un support
cher. Finalement, la durée allouée par le corps médical à un visiteur (environ 5 min)
requiert d'être rapide et efficace. La dématérialisation et l’équipement portable des
visiteurs médicaux, mis en place par les laboratoires permettent d’avoir une image plus
novatrice et dynamique.</p><p>
De plus, avec une solution logicielle, tel qu’un CLM, l’ensemble des informations
requises au bon déroulement d'une visite commerciale sont disponibles directement
pour le visiteur médical.</p><p>
Notre laboratoire a donc décidé d’avoir recours à un CLM, afin que nos visiteurs
médicaux soit plus dynamiques dans leur méthode commerciale. Cela nous permettra
de mieux connaitre l’intérêt et les attentes des prescripteurs et ainsi d’adapter les bons
contenus aux bons prescripteurs ce qui est le but principal d’une solution de Closed
Loop Marketing (CLM).</p><p>

Afin de mener à bien ce projet, nous avons choisi de comparer le CLM de Veeva et celui
de Viseven.
Ce sont tous deux des logiciels disponibles en anglais, ce qui concorde avec l'origine de
l'entreprise et facilite l'internationalisme.</p>
<img src="veeva.jpg" style="border: 1px solid black;" width="49%" height="100px">
<img src="viseven.jpg" style="border: 1px solid black;" width="49%" height="100px">
<p>
Il y a deux différences majeures entre ces 2 CLM, la première est
le fait que Viseven est spécialisé pour le domaine de la médecine,
tandis que Veeva l'est dans la gestion. La deuxième, le CLM de
Veeva est obligatoirement couplée à leur CRM et ils ne peuvent
donc pas être dissociés, contrairement à Viseven.</p><p>
Les deux sont à peu près équivalents dans les services qu’ils
offrent, il faut donc analyser l'aspect pratique.</p><p>
De nombreuses caractéristiques leurs sont communes, nous n'allons donc ici ne lister
que leurs différences.</p><p>
Les deux proposent un système de messagerie, Viseven la trie par chaines de
préférence et propose un système de retour d’information en direct pour les rendezvous,
ce qui pourrait également s'appliquer pour les visites auprès des prescripteurs.
Viseven propose également un planificateur, qui permet d’aider les visiteurs dans leur
gestion d'emploi du temps et ainsi d’être mieux organisé et plus actif. Sur ce point, Veeva
propose un aperçu facilitant les réunions, mais ne parle pas de planification.</p><p>
Viseven est disponible sur iOS, Android et Windows, Veeva n'est
disponible que sur iPad, Windows 7 et 10 ce qui nous laisse moins de
choix de matériel.</p><p>
Veeva possède un mode hors-ligne, très pratique pour les visiteurs qui
ne peuvent se permettre d'avoir internet partout où ils vont.</p><p>
Viseven propose d'autres avantages tels que la sécurité de la base de
données, l'accessibilité de celle-ci, un système basé sur le cloud et le
smart-analyse.</p>



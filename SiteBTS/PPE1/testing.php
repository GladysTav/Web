<h2>Le Closed-Loop Marketing (CLM)</h2>
<h3>Testing et choix d'une solution de CLM :</h3>
<p>Suite à cette étude, nous avons déterminé que Viseven semblait le plus adapté à nos
besoins. Nous avons donc décidé de tester cette solution et en sommes arrivé à la
conclusion que Viseven serait notre CLM définitif pour notre mission.</p><p>
En effet, il est très complet et assez simple et clair à utiliser pour les visiteurs médicaux.
La page d’accueil du CLM regroupe tout ce dont les visiteurs ont besoin. Les
notifications de rappel de rendez-vous sont écrites de manière claire lors du lancement
de l’application. On sait exactement ce qu’il y a de prévu dans la journée.</p><p>

On peut directement accéder à la liste de rendez-vous, aux contacts, aux comptes, à la
bibliothèque de documents. On peut également se synchroniser directement avec une
base de données grâce au cloud et également avoir accès à son planning.</p><p>
La partie contact est très bien organisée. Nous avons : le nom, le prénom, le téléphone,
l’email et le lieu de travail de chaque prescripteur, qui est relié à la partie compte afin de
savoir quel métier il effectue. On peut les classer par métier, spécialisation de
médecine, clinique …</p><p>
La partie compte permet de connaître surtout les différents lieux de travail des
prescripteurs Ce sont les adresses, les numéros de téléphone, les adresses email, les
sites internet et les type de lieux qui y sont enregistrés.</p><p>
La partie rendez-vous nous permet de voir tous les rendez-vous passé et à venir, ainsi
que la date, l’heure, le professionnel et le lieu. Tout ça est organisé de manière très
claire afin d’améliorer la rapidité des visiteurs médicaux, tout comme la partie
bibliothèque de documents qui permet d’avoir sous la main toute la documentation
nécessaire lors des visites.</p><p>
La partie planning est extrêmement bien conçue. On peut y enregistrer les rendez-vous
en précisant avec qui, où et quand. Lors de la programmation du rendez-vous, on peut y
indiquer le but de la visite et le dénouement à la fin de cet entretien. Le planning remonte
instantanément les rendez-vous qui ont eu lieu précédemment avec le même
prescripteur afin que le visiteur se rappelle du bilan de la dernière visite.</p><p>
 Une liste de
produits adaptés au prescripteur se génère et se place dans la case de produit
recommandé pour chaque professionnel. Il suffit de sélectionner le produit dans la
plage de rendez-vous pour avoir directement accès à la présentation de ce produit, ce
qui représente un gain de temps non négligeable.</p><p>

Ce test de Viseven nous a permis de constater que ce CLM était le plus adapté au projet
que nous souhaitons réaliser.</p><p>
De plus, sa compatibilité avec Windows, IOS et Android nous permet d’avoir une plus
grande marge de manœuvre dans le choix du matériel avec lequel nous allons équiper
nos équipes de visiteurs médicaux.</p>


